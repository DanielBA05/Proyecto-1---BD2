CREATE TABLE USUARIO (
    ID_USUARIO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    USERNAME VARCHAR2(50) NOT NULL,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    CORREO VARCHAR2(100) NOT NULL,
    TELEFONO VARCHAR2(20),
    NOMBRE VARCHAR2(50) NOT NULL,
    APELLIDO1 VARCHAR2(50) NOT NULL,
    APELLIDO2 VARCHAR2(50),
    ROL VARCHAR2(20) NOT NULL,
    CONSTRAINT PK_USUARIO PRIMARY KEY (ID_USUARIO),
    CONSTRAINT UQ_USUARIO_USERNAME UNIQUE (USERNAME),
    CONSTRAINT UQ_USUARIO_CORREO UNIQUE (CORREO),
    CONSTRAINT CHK_USUARIO_ROL CHECK (ROL IN ('DOCTOR', 'PACIENTE'))
);

CREATE TABLE DOCTOR (
    ID_DOCTOR NUMBER NOT NULL,
    CODIGO_PROFESIONAL VARCHAR2(50) NOT NULL,
    ESPECIALIDAD VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_DOCTOR PRIMARY KEY (ID_DOCTOR),
    CONSTRAINT FK_DOCTOR_USUARIO FOREIGN KEY (ID_DOCTOR)
        REFERENCES USUARIO(ID_USUARIO)
);

CREATE TABLE PACIENTE (
    ID_PACIENTE NUMBER NOT NULL,
    FECHA_NACIMIENTO DATE NOT NULL,
    SEXO VARCHAR2(10) NOT NULL,
    DIRECCION VARCHAR2(150),
    CONSTRAINT PK_PACIENTE PRIMARY KEY (ID_PACIENTE),
    CONSTRAINT FK_PACIENTE_USUARIO FOREIGN KEY (ID_PACIENTE)
        REFERENCES USUARIO(ID_USUARIO)
);

CREATE TABLE CONSULTORIO (
    ID_CONSULTORIO NUMBER NOT NULL,
    NOMBRE VARCHAR2(50) NOT NULL,
    UBICACION VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_CONSULTORIO PRIMARY KEY (ID_CONSULTORIO),
    CONSTRAINT UQ_CONSULTORIO_NOMBRE UNIQUE (NOMBRE)
);

CREATE TABLE BLOQUE_HORARIO (
    ID_BLOQUE NUMBER NOT NULL,
    ID_DOCTOR NUMBER NOT NULL,
    ID_CONSULTORIO NUMBER NOT NULL,
    INICIO_BLOQUE TIMESTAMP NOT NULL,
    FIN_BLOQUE TIMESTAMP NOT NULL,
    ESTADO_BLOQUE VARCHAR2(20) NOT NULL,
    CONSTRAINT PK_BLOQUE_HORARIO PRIMARY KEY (ID_BLOQUE),
    CONSTRAINT FK_BLOQUE_DOCTOR FOREIGN KEY (ID_DOCTOR)
        REFERENCES DOCTOR(ID_DOCTOR),
    CONSTRAINT FK_BLOQUE_CONSULTORIO FOREIGN KEY (ID_CONSULTORIO)
        REFERENCES CONSULTORIO(ID_CONSULTORIO),
    CONSTRAINT CHK_BLOQUE_ESTADO CHECK (ESTADO_BLOQUE IN ('RESERVADO', 'CANCELADO')),
    CONSTRAINT CHK_BLOQUE_FECHAS CHECK (INICIO_BLOQUE < FIN_BLOQUE)
);

CREATE TABLE CITA (
    ID_CITA NUMBER NOT NULL,
    ID_BLOQUE NUMBER NOT NULL,
    ID_PACIENTE NUMBER,
    ESTADO_CITA VARCHAR2(20) NOT NULL,
    INICIO_CITA TIMESTAMP NOT NULL,
    FIN_CITA TIMESTAMP NOT NULL,
    CONSTRAINT PK_CITA PRIMARY KEY (ID_CITA),
    CONSTRAINT FK_CITA_BLOQUE FOREIGN KEY (ID_BLOQUE)
        REFERENCES BLOQUE_HORARIO(ID_BLOQUE),
    CONSTRAINT FK_CITA_PACIENTE FOREIGN KEY (ID_PACIENTE)
        REFERENCES PACIENTE(ID_PACIENTE),
    CONSTRAINT CHK_CITA_ESTADO CHECK (ESTADO_CITA IN ('DISPONIBLE', 'RESERVADA', 'CANCELADA', 'ATENDIDA', 'AUSENTE')),
    CONSTRAINT CHK_CITA_FECHAS CHECK (INICIO_CITA < FIN_CITA)
);

CREATE TABLE PRUEBA (
    ID NUMBER NOT NULL,
    NOMBRE VARCHAR2(100),
    CONSTRAINT PK_PRUEBA PRIMARY KEY (ID)
);

CREATE SEQUENCE SEQ_USUARIO START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE SEQ_CONSULTORIO START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE SEQ_BLOQUE_HORARIO START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;
CREATE SEQUENCE SEQ_CITA START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

CREATE OR REPLACE TRIGGER TRG_USUARIO_BI
BEFORE INSERT ON USUARIO
FOR EACH ROW
BEGIN
    IF :NEW.ID_USUARIO IS NULL THEN
        SELECT SEQ_USUARIO.NEXTVAL INTO :NEW.ID_USUARIO FROM DUAL;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_CONSULTORIO_BI
BEFORE INSERT ON CONSULTORIO
FOR EACH ROW
BEGIN
    IF :NEW.ID_CONSULTORIO IS NULL THEN
        SELECT SEQ_CONSULTORIO.NEXTVAL INTO :NEW.ID_CONSULTORIO FROM DUAL;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_BLOQUE_BI
BEFORE INSERT ON BLOQUE_HORARIO
FOR EACH ROW
BEGIN
    IF :NEW.ID_BLOQUE IS NULL THEN
        SELECT SEQ_BLOQUE_HORARIO.NEXTVAL INTO :NEW.ID_BLOQUE FROM DUAL;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_CITA_BI
BEFORE INSERT ON CITA
FOR EACH ROW
BEGIN
    IF :NEW.ID_CITA IS NULL THEN
        SELECT SEQ_CITA.NEXTVAL INTO :NEW.ID_CITA FROM DUAL;
    END IF;
END;
/

CREATE INDEX IDX_BLOQUE_RANGO
ON BLOQUE_HORARIO (INICIO_BLOQUE, FIN_BLOQUE);

CREATE INDEX IDX_CITA_RANGO
ON CITA (ID_BLOQUE, INICIO_CITA, FIN_CITA);

CREATE INDEX IDX_CITA_ESTADO
ON CITA (ESTADO_CITA);